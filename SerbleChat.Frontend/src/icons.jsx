// Central icon registry — all UI icons as SVG React components.
// Each component accepts: size (default 16), className, style props.
// They use currentColor for fill/stroke so they inherit CSS color.

function Svg({ size = 16, className, style, children, fill = 'none', strokeWidth = 2, viewBox = '0 0 24 24', ...rest }) {
  return (
    <svg
      xmlns="http://www.w3.org/2000/svg"
      viewBox={viewBox}
      width={size}
      height={size}
      fill={fill}
      stroke="currentColor"
      strokeWidth={strokeWidth}
      strokeLinecap="round"
      strokeLinejoin="round"
      className={className}
      style={{ display: 'inline-block', verticalAlign: 'middle', flexShrink: 0, ...style }}
      aria-hidden="true"
      {...rest}
    >
      {children}
    </svg>
  );
}

